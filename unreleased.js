let currentProjectIndex = 0;
let currentTrackIndex = -1;
let isPlaying = false;

const audio = new Audio();
audio.volume = 1;

const projectShortcuts = document.getElementById("projectShortcuts");
const projectYear = document.getElementById("projectYear");
const projectName = document.getElementById("projectName");
const projectStatus = document.getElementById("projectStatus");
const projectCover = document.getElementById("projectCover");
const projectDescription = document.getElementById("projectDescription");
const tracklistBox = document.getElementById("tracklistBox");

const nowPlayingTitle = document.getElementById("nowPlayingTitle");
const nowPlayingProject = document.getElementById("nowPlayingProject");
const playPauseBtn = document.getElementById("playPauseBtn");
const currentTimeEl = document.getElementById("currentTime");
const durationEl = document.getElementById("duration");
const playerBar = document.getElementById("playerBar");
const playerBarFill = document.getElementById("playerBarFill");
const volumeSlider = document.getElementById("volumeSlider");

function renderShortcuts() {
    projectShortcuts.innerHTML = "";

    projects.forEach((project, index) => {
        const button = document.createElement("button");
        button.className = `project-shortcut ${index === currentProjectIndex ? "active" : ""}`;
        button.type = "button";
        button.setAttribute("aria-label", `Wybierz projekt ${project.title}`);
        button.innerHTML = `<img src="${project.cover}" alt="${project.title} cover">`;

        button.addEventListener("click", () => {
            currentProjectIndex = index;
            currentTrackIndex = -1;
            audio.pause();
            audio.removeAttribute("src");
            audio.load();

            renderShortcuts();
            renderActiveProject();
            resetAudioBox();
        });

        projectShortcuts.appendChild(button);
    });
}

function renderActiveProject() {
    const project = projects[currentProjectIndex];

    projectYear.textContent = project.year;
    projectName.textContent = project.title;
    projectStatus.textContent = project.status;
    projectCover.src = project.cover;
    projectCover.alt = `${project.title} cover`;
    projectDescription.textContent = project.description;

    renderTracklist();
}

function renderTracklist() {
    const project = projects[currentProjectIndex];
    tracklistBox.innerHTML = "";

    project.tracks.forEach((track, index) => {
        const row = document.createElement("div");
        row.className = "track-row";
        row.dataset.index = index;

        row.innerHTML = `
            <span class="track-number">${String(index + 1).padStart(2, "0")}</span>
            <button class="btn btn-outline-dark btn-sm rounded-0 track-play-btn" type="button" aria-label="Play track">
                <i class="bi bi-play-fill"></i>
            </button>
            <span class="track-title">${track.title}</span>
            <span class="track-duration">${track.duration}</span>
        `;

        const playBtn = row.querySelector(".track-play-btn");
        playBtn.addEventListener("click", () => {
            if (currentTrackIndex === index && isPlaying) {
                audio.pause();
            } else if (currentTrackIndex === index && !isPlaying && audio.src) {
                audio.play();
            } else {
                playTrack(index);
            }
        });

        tracklistBox.appendChild(row);
    });

    updateActiveTrackHighlight();
}

function playTrack(trackIndex) {
    const project = projects[currentProjectIndex];
    const track = project.tracks[trackIndex];

    currentTrackIndex = trackIndex;
    audio.src = track.src;

    nowPlayingTitle.textContent = track.title;
    nowPlayingProject.textContent = project.title;

    audio.play()
        .then(() => {
            isPlaying = true;
            updatePlayPauseButton();
            updateActiveTrackHighlight();
        })
        .catch(() => {
            isPlaying = false;
            updatePlayPauseButton();
            updateActiveTrackHighlight();
        });
}

function togglePlayPause() {
    if (!audio.src) return;

    if (isPlaying) {
        audio.pause();
        isPlaying = false;
    } else {
        audio.play()
            .then(() => {
                isPlaying = true;
                updatePlayPauseButton();
                updateActiveTrackHighlight();
            })
            .catch(() => {
                isPlaying = false;
                updatePlayPauseButton();
                updateActiveTrackHighlight();
            });
        return;
    }

    updatePlayPauseButton();
    updateActiveTrackHighlight();
}

function updatePlayPauseButton() {
    playPauseBtn.innerHTML = isPlaying
        ? '<i class="bi bi-pause-fill"></i>'
        : '<i class="bi bi-play-fill"></i>';
}

function formatTime(time) {
    if (isNaN(time)) return "0:00";
    const minutes = Math.floor(time / 60);
    const seconds = Math.floor(time % 60).toString().padStart(2, "0");
    return `${minutes}:${seconds}`;
}

function resetAudioBox() {
    nowPlayingTitle.textContent = "Wybierz utwór";
    nowPlayingProject.textContent = "Brak aktywnego utworu";
    currentTimeEl.textContent = "0:00";
    durationEl.textContent = "0:00";
    playerBarFill.style.width = "0%";
    isPlaying = false;
    updatePlayPauseButton();
    updateActiveTrackHighlight();
}

function updateActiveTrackHighlight() {
    const rows = tracklistBox.querySelectorAll(".track-row");

    rows.forEach((row) => {
        const rowIndex = Number(row.dataset.index);
        row.classList.toggle("is-active", rowIndex === currentTrackIndex);

        const btn = row.querySelector(".track-play-btn");
        if (!btn) return;

        if (rowIndex === currentTrackIndex && isPlaying) {
            btn.innerHTML = '<i class="bi bi-pause-fill"></i>';
        } else {
            btn.innerHTML = '<i class="bi bi-play-fill"></i>';
        }
    });
}

audio.addEventListener("loadedmetadata", () => {
    durationEl.textContent = formatTime(audio.duration);
});

audio.addEventListener("timeupdate", () => {
    currentTimeEl.textContent = formatTime(audio.currentTime);

    if (audio.duration) {
        const progress = (audio.currentTime / audio.duration) * 100;
        playerBarFill.style.width = `${progress}%`;
    }
});

audio.addEventListener("pause", () => {
    isPlaying = false;
    updatePlayPauseButton();
    updateActiveTrackHighlight();
});

audio.addEventListener("play", () => {
    isPlaying = true;
    updatePlayPauseButton();
    updateActiveTrackHighlight();
});

playerBar.addEventListener("click", (event) => {
    if (!audio.duration) return;

    const rect = playerBar.getBoundingClientRect();
    const clickX = event.clientX - rect.left;
    const width = rect.width;
    const percentage = clickX / width;

    audio.currentTime = percentage * audio.duration;
});

volumeSlider.addEventListener("input", () => {
    audio.volume = Number(volumeSlider.value);
});

playPauseBtn.addEventListener("click", togglePlayPause);

renderShortcuts();
renderActiveProject();
resetAudioBox();